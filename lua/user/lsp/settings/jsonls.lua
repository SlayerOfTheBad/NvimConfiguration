-- Find more schemas: https://www.schemastore.org/json/
local schemas = {
  {
    description = "Package.json configuration for npm projects",
    filematch = {
      "package.json",
    },
    url = "https://json.schemastore.org/package.json",
  },
}

local opts = {
  settings = {
    json = {
      schemas = schemas,
    },
  },
  setup = {
    commands = {
      Format = {
        function()
          vim.lsp.buf.range_formatting({}, { 0, 0 }, { vim.fn.line "$", 0 })
        end,
      },
    },
  },
}

return opts
